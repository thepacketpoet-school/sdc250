FROM TWOS t 
RIGHT OUTER JOIN THREES th 
ON t.NUMBER_2 = th.NUMBER_3;

-- (14-5) Syntax for Full Outer Join
SELECT t.NUMBER_2, t.WORD_2, th.NUMBER_3, th.WORD_3 
FROM TWOS t 
FULL OUTER JOIN THREES th 
ON t.NUMBER_2 = th.NUMBER_3;

-- (14-6) Writing a UNION in SQL
SELECT NUMBER_2 AS NUM, WORD_2 AS WORD FROM TWOS
UNION
SELECT NUMBER_3, WORD_3 FROM THREES;

