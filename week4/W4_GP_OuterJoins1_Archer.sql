-- (14-1) Inner Join of TWOS and THREES
SELECT t.NUMBER_2, t.WORD_2, th.NUMBER_3, th.WORD_3 
FROM TWOS t 
INNER JOIN THREES th 
ON t.NUMBER_2 = th.NUMBER_3;

-- (14-2) Left, Right, and Full Outer Joins

-- Left Outer Join
SELECT t.NUMBER_2, t.WORD_2, th.NUMBER_3, th.WORD_3 
FROM TWOS t 
LEFT OUTER JOIN THREES th 
ON t.NUMBER_2 = th.NUMBER_3;

-- Right Outer Join
SELECT t.NUMBER_2, t.WORD_2, th.NUMBER_3, th.WORD_3 
FROM TWOS t 
RIGHT OUTER JOIN THREES th 
ON t.NUMBER_2 = th.NUMBER_3;

-- Full Outer Join
SELECT t.NUMBER_2, t.WORD_2, th.NUMBER_3, th.WORD_3 
FROM TWOS t 
FULL OUTER JOIN THREES th 
ON t.NUMBER_2 = th.NUMBER_3;

-- (14-3) Syntax for Left Outer Join
SELECT t.NUMBER_2, t.WORD_2, th.NUMBER_3, th.WORD_3 
